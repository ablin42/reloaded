int	ft_sqrt(int nb)
{
	int tmp;

	tmp = nb;
	if (nb >= 2147483647)
		return (0);
	while (nb > 0)
	{
		if (nb * nb == tmp)
			return (nb);
		nb--;
	}
	return (0);
}
